def add_or_concatenate():
    try:
        # Запитуємо користувача ввести два значення
        num1 = float(input("Введіть перше число: "))
        num2 = float(input("Введіть друге число: "))

        # Підсумовуємо числа, якщо вони обидва є числами
        result = num1 + num2

        # Виводимо результат
        print(f"Сума введених чисел: {result}")

    except ValueError:
        # Обробляємо виняток, якщо введене значення не є числом
        str1 = input("Введіть перший рядок: ")
        str2 = input("Введіть другий рядок: ")

        # Конкатенуємо рядки, якщо хоча б одне з них не є числом
        result = str1 + str2

        # Виводимо результат
        print(f"Результат конкатенації: {result}")


# Викликаємо функцію
add_or_concatenate()
